package com.shawnclake.morgencore.core.component;

/**
 * 1 dimensional vector (x)
 */
public class Vector1D {

    private float x;

    public Vector1D(float x) {
        this.x = x;
    }

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

}
